import { existsSync, mkdirSync, readFileSync, writeFileSync } from 'node:fs';
import { join } from 'node:path';

// Release checker marker: stable `1.2.0` promotion

const root = process.cwd();
const rootPackage = JSON.parse(readFileSync(join(root, 'package.json'), 'utf8'));
const version = process.env.RELEASE_VERSION ?? rootPackage.version;
const outDir = join(root, 'release-manifests');
const statusPath = join(outDir, `ariana-${version}-release-status.md`);

mkdirSync(outDir, { recursive: true });

const items = [
  ['Root package version', rootPackage.version === version, `package.json version is ${rootPackage.version}`],
  ['Version task list', exists('docs/releases/1.2.0-version-tasks.md'), 'docs/releases/1.2.0-version-tasks.md'],
  ['Manual validation template', exists('docs/releases/1.2.0-rc.1-manual-validation.md'), 'docs/releases/1.2.0-rc.1-manual-validation.md'],
  ['Stable promotion plan', exists('docs/releases/1.2.0-stable-promotion-plan.md'), 'docs/releases/1.2.0-stable-promotion-plan.md'],
  ['Version task checker', exists('scripts/check-v1-2-version-tasks.mjs'), 'scripts/check-v1-2-version-tasks.mjs'],
  ['RC release notes', exists('docs/releases/1.2.0-rc.1.md'), 'docs/releases/1.2.0-rc.1.md'],
  ['RC checklist', exists('docs/releases/1.2.0-rc.1-checklist.md'), 'docs/releases/1.2.0-rc.1-checklist.md'],
  ['Release sequence', exists('docs/releases/1.2.0-rc.1-release-sequence.md'), 'docs/releases/1.2.0-rc.1-release-sequence.md'],
  ['Artifact verification guide', exists('docs/releases/1.2.0-rc.1-artifact-verification.md'), 'docs/releases/1.2.0-rc.1-artifact-verification.md'],
  ['Candidate workflow', exists('.github/workflows/release-v1-2-rc.yml'), '.github/workflows/release-v1-2-rc.yml'],
  ['NPM dry run workflow', exists('.github/workflows/release-v1-2-npm-dry-run.yml'), '.github/workflows/release-v1-2-npm-dry-run.yml'],
  ['Protected next workflow', exists('.github/workflows/release-v1-2-next.yml'), '.github/workflows/release-v1-2-next.yml'],
  ['GitHub draft release workflow', exists('.github/workflows/release-v1-2-github-release.yml'), '.github/workflows/release-v1-2-github-release.yml'],
  ['NPM registry verification workflow', exists('.github/workflows/release-v1-2-verify.yml'), '.github/workflows/release-v1-2-verify.yml'],
  ['Workflow hygiene workflow', exists('.github/workflows/workflow-hygiene.yml'), '.github/workflows/workflow-hygiene.yml'],
  ['Packed content verifier', exists('scripts/verify-packed-tarball-contents.mjs'), 'scripts/verify-packed-tarball-contents.mjs'],
  ['Packed dependency verifier', exists('scripts/verify-packed-internal-dependencies.mjs'), 'scripts/verify-packed-internal-dependencies.mjs'],
  ['Packed install smoke', exists('scripts/packed-candidate-smoke.mjs'), 'scripts/packed-candidate-smoke.mjs'],
  ['Manifest generator', exists('scripts/create-release-manifest.mjs'), 'scripts/create-release-manifest.mjs'],
  ['Manifest verifier', exists('scripts/verify-release-manifest.mjs'), 'scripts/verify-release-manifest.mjs'],
  ['Summary generator', exists('scripts/create-release-summary.mjs'), 'scripts/create-release-summary.mjs'],
  ['Release candidate gate', exists('scripts/check-v1-2-release-candidate.mjs'), 'scripts/check-v1-2-release-candidate.mjs']
];

const rows = items.map(([label, ok, detail]) => `| ${ok ? 'PASS' : 'FAIL'} | ${label} | ${detail} |`);
const failed = items.filter(([, ok]) => !ok);

const report = `# Ariana ${version} Release Status

Generated by \`scripts/create-v1-2-release-status.mjs\`.

## Summary

${failed.length === 0 ? 'All tracked release assets are present.' : `${failed.length} tracked release asset(s) are missing.`}

## Checks

| Status | Check | Detail |
| --- | --- | --- |
${rows.join('\n')}

## Next operator steps

1. Run local release gates.
2. Run the candidate workflow with gates enabled.
3. Run the npm dry run workflow with the \`next\` tag.
4. Fill \`docs/releases/1.2.0-rc.1-manual-validation.md\` after manual checks.
5. Run manual UI checks for \`/animate\` and \`/route-outlet\`.
6. Create the GitHub draft release.
7. Publish the RC to npm \`next\` only after approval.
8. Verify npm registry state.
9. Collect feedback before stable \`1.2.0\` promotion.
`;

writeFileSync(statusPath, report);
console.log(`Ariana ${version} release status written to ${statusPath}`);

if (failed.length > 0) process.exit(1);

function exists(path) {
  return existsSync(join(root, path));
}
